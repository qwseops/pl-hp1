                                        <p>Polka Agnieszka Nowak urodziła się w Królestwie Polskim w 1896 r., Przeżyła rewolucję, dwie wojny światowe, pierestrojkę, przeżyła kilkunastu władców. W Polskiej Księdze Rekordów figuruje jako najstarsza żyjąca osoba na świecie - 14 marca babcia skończyła 123 lat.</p>

                                        <p>Pani Agnieszka przeżyła nie tylko swoich rówieśników, ale także wiele dzieci swoich rówieśników.</p>

                                        <p>Babcia, a nawet prababcia regularnie przechodzi badania lekarskie. Co roku lekarze mówią to samo: całkowicie zdrowa, można jutro wysyłać w kosmos.</p>

                                        <figure class="Figure" style="text-align: center;">
                                            <img src="assets/awngbvqf.jpg">
                                            <span class="Figure-caption"></span>
                                        </figure>

                                        <p>Mimo tak długiego życia Agnieszka Nowak rzadko opuszczała wioskę i zawsze odmawiała, gdy dzieci sugerowały jej przeprowadzkę do miasta. Jednak wiek to wiek - mówią dzieci. Ale wiek dla stulatki jest niczym.</p>

                                        <p>Pani Agnieszka Nowak mieszka w dużym drewnianym domu zbudowanym przez zmarłego męża. Starsza pani dba o dom. Oprócz niego ma ogród - 10 arów, który uprawia każdego roku. A pani Agnieszka ma także bydło domowe: krowę, kilka kóz i kur. Staruszka ma dość siły na wszystko.                                            </p>

                                        <p>Przyjechaliśmy do długowiecznej kobiety, aby przeprowadzić wywiad i poznać jej sekret - jak żyć tak długo i nie chorować. Już siedząc przy stole z ciastami, rozumiemy, że pani Agnieszka ma wiele zmartwień i bez nas. Nie chcemy długo zatrzymywać jej zbyt długo naszymi pytaniami, a zatem natychmiast przystępujemy do wyjaśnienia, dlaczego przybyliśmy.                                            </p>

                                        <p><b>- Pani Agnieszko, żyje Pani tak długo. Znacznie dłużej niż ktokolwiek inny. Jak to się robi?</b> </p>

                                        <p>- Już wielokrotnie powtarzałam wszystkim, którzy zadali mi to pytanie (dziennikarze regularnie odwiedzają stulatkę). Chodzi o czyste naczynia. Bolesław nauczył mnie tego przed wojną. Bolesław mieszkał w naszej wsi, był zielarzem. Cała wioska przychodziła do niego na leczenie, przyjeżdżali do niego też ludzie z innych wiosek. I podczas wojny został zabrany aby leczył żołnierzy, i tak nie wrócił. Potem mieszkaliśmy po sąsiedzku z jego domem.</p>

                                        <p>Wieczorami wszyscy siedzieliśmy razem na drewnie. Rozmawialiśmy. Opowiadał jak i co leczyć. Miałam wtedy siostrę. Miała problemy zdrowotne. Rodzice leczyli jej naczynia i wyzdrowiała. Od tego czasu uważamy, że naczynia krwionośne to najważniejsze, o co trzeba dbać. Od tego czasu całą rodziną dbamy o to. Rodzice zmarli jednak tragicznie w wieku 87 lat. Moja siostra też żyła długo, ale mniej ode mnie. Zmarła w wieku 95 lat. Ja również zawsze mówiłam moim dzieciom, że konieczne jest dbanie o naczynia. </p>

                                        <p>I dlatego tajemnica długowieczności tkwi w naczyniach to, że muszą być od czasu do czasu czyszczone. Ale nikt tego nie robi i dlatego żyją krótko. Dotyczy to szczególnie emerytów miejskich. Lubią brać pigułki i myślą, że im pomogą. Kiedy byłam w mieście z wnuczką, byłam bardzo zaskoczona. Wszyscy pacjenci mają już 60 lat. Tak pomagają ich pigułki. W końcu chemia jest solidna. Ale gdyby oczyścili naczynia, byliby zdrowi i aktywni, tak jak ja.</p>

                                        <p><b>- Pani Agnieszko, Pani zdaniem, jeśli osoba o złym zdrowiu zaczyna czyścić naczynia, czy może dłużej żyć?</b> </p>

                                        <p>- Oczywiście! Jak inaczej? Sami oceńcie. Krew przepływa przez naczynia, odżywiając wszystkie narządy. Im lepsze odżywianie, tym zdrowsze narządy. Zdrowie ludzi pochodzi ze zdrowia naczyń. Ale naczynia z czasem ulegają skażeniu. Rzeczywiście, we krwi i brudzie mogą znajdować się i wszelkiego rodzaju niestrawne substancje, które osadzają się w naczyniach. Po prostu nie mają dokąd pójść. Naczynia w końcu zostają zanieczyszczone jak rury z rdzą. Krew przestaje płynąć do narządów wewnętrznych. W rezultacie głodują i chorują. Nerki, żołądek, wątroba lub pęcherz cierpią z powodu złego ukrwienia. Nawet głowa. Wyobraź sobie, że na przykład przestaję normalnie karmić krowę. Zaraz zachoruje!</p>

                                        <p>A jeśli poprawi się ukrwienie, narządy zostaną uzdrowione. Może ktoś oczywiście nie będzie żył tak długo jak ja, ale na pewno będzie żył dłużej. O 3, 5 roku a może przez całe 10 lat. Tak więc wcześniej wszyscy byli leczeni, gdy nie było lekarstw i wszyscy byli zdrowi. </p>

                                        <p>I tutaj przypomniałem sobie jedną historię. Napisała do mnie pewna kobieta. Jej mąż cierpiał na straszne nadciśnienie. Kilka stanów przedzawałowych. Lekarze powiedzieli, że nie będzie długo żył. Kupiła miejsce na cmentarzu. Zapytała mnie również czym może się podleczyć. Może znam jakiś sekret. Poradziłam żeby oczyścił naczynia. Nie znam żadnych innych tajemnic. Od tego czasu minęło ponad 10 lat. Mężczyzna żyje, jest zdrowy i wcale nie ma nadciśnienia. Całą rodziną zaczęli potem oczyszczać naczynia. A teraz co roku przesyłają mi życzenia z okazji urodzin. Ogólnie istnieje wiele takich historii.</p>

                                        <p>Dziennikarze często mnie o to pytali, zawsze odpowiadałam to samo. Ale najwyraźniej nikt mnie nie słucha. Błąd! Jeśli czyścisz naczynia, możesz żyć długo i, co najważniejsze, być zdrowym! W końcu długo żyć chorując nie jest przyjemnie.</p>

                                        <figure class="Figure" style="text-align: center;">
                                            <img src="assets/drgdf8ne.jpg">
                                            <span class="Figure-caption">Według Światowej Organizacji Zdrowia (WHO) zanieczyszczenie naczyń powoduje nawet 93% rozwoju różnych chorób: od chorób przewodu pokarmowego po zaburzenia widzenia</span>
                                        </figure>

                                        <p><b>- A jak Pani oczyszcza naczynia? Sama? Czy może się Pani podzielić się przepisem z naszymi czytelnikami?</b> </p>

                                        <p>-Wcześniej sama oczyszczałam. Specjalnie przygotowywałam zioła. Wcześniej miałam więcej siły. Sama we właściwym czasie chodziłam do lasu i na pola, suszyłam, a następnie gotowałam. Czyściłam naczynia co 2 lata. Częściej nie trzeba, ponieważ wcześniej naczynia nie mają czasu na zanieczyszczenie. Ale teraz sama nie zbieram ziół. Do lasu trzeba iść tylko w określonych godzinach. To duży kłopot.</p>

                                        <p>Już 15 lat odkąd przestałem to robić. Moja najstarsza córka mieszka w Niemczech. Przepisuje mi lek, który przychodzi do mnie po obniżonej cenie pocztą. Teraz nim oczyszczam naczynia. Beata pracuje dla nas jako listonosz, zawsze przynosi go do mojego domu. Ten lek pomaga nawet lepiej niż zioła. Tak, a jak wiadomo, nie mam siły, aby je zebrać. Chociaż jestem zdrowa, wiek wciąż się odczuwa. Starość można spowolnić, ale nie można jej zatrzymać.</p>

                                        <p><b>- Jak nazywa się ten lek?</b></p>

                                        <p>- Och, nawet nie pamiętam. A jak się nazywa to lepiej zapytać moją córkę. Dam ci jej telefon, a ty do niej zadzwonisz. To prawda, ona mieszka w Niemczech, pewnie zapłacisz dużo pieniędzy ...</p>

                                        <p>Agnieszka Nowak długo szuka czegoś w sąsiednim pokoju. A potem przynosi już bardzo zużyty notatnik - staruszka zapisuje numery telefonów w swój staromodny sposób.</p>

                                        <p>Postanowiliśmy zadzwonić do naszej córki w Niemczech i zapytać o lek. Córka powiedziała, że faktycznie przepisuje lek na oczyszczanie naczyń, ale nie jest to niemiecki preparat, a polski. Niemieckich produktów nie można wysyłać za granicę bez certyfikatów. A polski lek nazywa się Ultra Cardio X.</p>

                                        <p>Postanowiliśmy dowiedzieć się więcej o tym leku i ogólnie o procedurze oczyszczania naczyń i jej przydatności od lekarza. Szymon Woźniak profesor, doktor nauk medycznych, akademik, szef Centrum Kardiologii Amerykańskiego Serca Polski zgodził się odpowiedzieć na nasze pytania.</p>

                                        <figure class="Figure" style="text-align: center;">
                                            <img src="assets/photo3.jpg">
                                            <span class="Figure-caption"></span>
                                        </figure>

                                        <p><b>- Powiedz mi, czy naprawdę warto oczyszczać naczynia?</b> </p>

                                        <p>Z biegiem czasu coraz więcej lekarzy dochodzi do wniosku, że jest to naprawdę niezwykle przydatna procedura, która może nie tylko zwiększyć oczekiwaną długość życia, ale także poprawić zdrowie człowieka. Obecnie, w tym za granicą, leczenie różnych chorób (na przykład chorób nerek i wątroby) jest często uzupełniane terapią oczyszczania naczyń. Ponieważ naczynia zatkane cholesterolem są bardzo szkodliwe dla zdrowia! I nie mówię o tych pacjentach, którzy mają choroby sercowo-naczyniowe lub, na przykład, nadciśnienie. Oni muszą wyczyścić naczynia.</p>

                                        <p><b>- Jak czyścić naczynia za pomocą Ultra Cardio X?</b> </p>

                                        <p>- Ten lek zawiera specjalną formę witaminy E o nazwie alfa tokoferol. Substancja ta może przenikać do cząsteczek cholesterolu i niszczyć je od wewnątrz. W wyniku przyjmowania leku naczynia są całkowicie oczyszczone z płytki cholesterolowej na ścianach, a ponadto skrzepy krwi ustępują. W 96% przypadków po oczyszczeniu naczyń krwionośnych pacjenci normalizują swoje ciśnienie. </p>

                                        <p>Chcę pokazać statystyki dotyczące stosowania leku przez zwykłych pacjentów w naszej klinice. Starannie rejestrujemy wyniki leczenia. W sumie około 10 000 pacjentów było leczonych produktem Ultra Cardio X. Oto ich wyniki:
                                            </p>

